public class ForIndexDEmo {
    public static void main(String[] args) {
        for(int i=1;i<=10;i++){
            System.out.println(i);
        }

        // lap cac phan tu trong array
        int[] numbers={4,9,6,2,5,1};
        for(int i=0; i<numbers.length;i++){
            System.out.println(numbers[i]);
        }
    }
}
